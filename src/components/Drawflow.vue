<template>

  <el-container>
    <el-header class="header">
        <h3>QemuWeb</h3>
        <el-button type="primary"   @click="exportEditor">Export</el-button>
    </el-header>
    <el-container class="container">
      <el-aside width="110px" class="column">
          <ul>
              <li v-for="n in listNodes" :key="n" draggable="true" :data-node="n.item" @dragstart="drag($event)" class="drag-drawflow" >
                  <div class="node" :style="`background: ${n.color};text-align: center`" >{{ n.name }}</div>
              </li>
          </ul>
          <!--<div class="terminal-container" data-port="43373"></div>-->
      </el-aside>
      <el-main>
          <div id="drawflow" @drop="drop($event)" @dragover="allowDrop($event)"></div>
      </el-main>
    </el-container>
  </el-container>
  <el-dialog v-model="dialogVisible" title="Export" width="50%" >
    <span>Data:</span>
    <pre><code>{{dialogData}}</code></pre>
    <template #footer>
      <span class="dialog-footer">
        <el-button @click="dialogVisible = false"> Cancel </el-button>
        <el-button type="primary" @click="dialogVisible = false"> Confirm </el-button>
      </span>
    </template>
  </el-dialog>
</template>
<script>

import drawflow from 'drawflow'
import styleDrawflow from 'drawflow/dist/drawflow.min.css'
import style from '../assets/style.css' 
import { onMounted, shallowRef, h, getCurrentInstance, render, readonly, ref } from 'vue'
import Host from './nodes/HostComp.vue'
import Switch from './nodes/SwitchComp.vue'
import { TerminalUI } from "../TerminalUI"; 
import io from "socket.io-client";


export default {
  name: 'DrawFlow',
  data() {
    return {
      serverAddress: "127.0.0.1"
    }
  },
  methods: {
    connectToSocket(serverAddr) {
      return new Promise(res => {
        const socket = io(serverAddr);
        res(socket);
      });
    },
    startTerminal(container, socket) {
      const terminal = new TerminalUI(socket);
      terminal.attachTo(container);
      console.log("startTerminal")
      terminal.startListening();
    },
    start() {
      var containers = document.getElementsByClassName("terminal-container");

      if (containers == undefined || containers.length < 1) {
        console.log("Pas de container pour le terminal trouvÃ©");
        return;
      }
      for (let container of containers) {
        console.log("connect on " + this.serverAddress + ":" + container.getAttribute("data-port"))
        this.connectToSocket(this.serverAddress + ":" + container.getAttribute("data-port")).then(socket => {
          this.startTerminal(container, socket);
        });
      }
    }
  },
  mounted() {
    this.start();
  },
  setup() {
    const listNodes = readonly([
      {
        name: 'PC',
        color: '#49494970',
        item: 'Host',
        input:1,
        output:1
      },
      {
        name: 'SWITCH',
        color: 'blue',
        item: 'Switch',
        input:1,
        output:1
      }
    ])
    
    const editor = shallowRef({})
    const dialogVisible = ref(false)
    const dialogData = ref({})
    const Vue = { version: 3, h, render };
    const internalInstance = getCurrentInstance()
    internalInstance.appContext.app._context.config.globalProperties.$df = editor;
    
    function exportEditor() {
      dialogData.value = editor.value.export();
      dialogVisible.value = true;
    }

    const drag = (ev) => {
      if (ev.type === "touchstart") {
        mobile_item_selec = ev.target.closest(".drag-drawflow").getAttribute('data-node');
      } else {
        ev.dataTransfer.setData("node", ev.target.getAttribute('data-node'));
      }
    }
    const drop = (ev) => {
      if (ev.type === "touchend") {
        var parentdrawflow = document.elementFromPoint( mobile_last_move.touches[0].clientX, mobile_last_move.touches[0].clientY).closest("#drawflow");
        if(parentdrawflow != null) {
          addNodeToDrawFlow(mobile_item_selec, mobile_last_move.touches[0].clientX, mobile_last_move.touches[0].clientY);
        }
        mobile_item_selec = '';
      } else {
        ev.preventDefault();
        var data = ev.dataTransfer.getData("node");
        addNodeToDrawFlow(data, ev.clientX, ev.clientY);
      }
    }
    const allowDrop = (ev) => {
      ev.preventDefault();
    }

    let mobile_item_selec = '';
    let mobile_last_move = null;
    function positionMobile(ev) {
      mobile_last_move = ev;
    }

    function addNodeToDrawFlow(name, pos_x, pos_y) {
      pos_x = pos_x * ( editor.value.precanvas.clientWidth / (editor.value.precanvas.clientWidth * editor.value.zoom)) - (editor.value.precanvas.getBoundingClientRect().x * ( editor.value.precanvas.clientWidth / (editor.value.precanvas.clientWidth * editor.value.zoom)));
      pos_y = pos_y * ( editor.value.precanvas.clientHeight / (editor.value.precanvas.clientHeight * editor.value.zoom)) - (editor.value.precanvas.getBoundingClientRect().y * ( editor.value.precanvas.clientHeight / (editor.value.precanvas.clientHeight * editor.value.zoom)));
    
      const nodeSelected = listNodes.find(ele => ele.item == name);
      editor.value.addNode(name, nodeSelected.input,  nodeSelected.output, pos_x, pos_y, name, {}, name, 'vue');
    }


    onMounted(() => {

      var elements = document.getElementsByClassName('drag-drawflow');
      for (var i = 0; i < elements.length; i++) {
        elements[i].addEventListener('touchend', drop, false);
        elements[i].addEventListener('touchmove', positionMobile, false);
        elements[i].addEventListener('touchstart', drag, false );
      }
        
        const id = document.getElementById("drawflow");

        editor.value = new drawflow(id, Vue, internalInstance.appContext.app._context);
        editor.value.force_first_input = true;
        editor.value.start();
        
        editor.value.registerNode('Host', Host, {}, {});
        editor.value.registerNode('Switch', Switch, {}, {});

        //editor.value.import({"drawflow":{"Home":{"data":{"5":{"id":5,"name":"Switch","data":{"script":"(req,res) => {\n console.log(req);\n}"},"class":"Switch","html":"Switch","typenode":"vue","inputs":{"input_1":{"connections":[{"node":"6","input_1":"output_1"}]}},"outputs":{"output_1":{"connections":[]},"output_1":{"connections":[]}},"pos_x":1000,"pos_y":117},"6":{"id":6,"name":"Host","data":{"url":"localhost/add", "method": "post"},"class":"Host","html":"Host","typenode":"vue","inputs":{},"outputs":{"output_1":{"connections":[{"node":"5","output":"input_1"}]}},"pos_x":137,"pos_y":89}}}}})
    })

    return {
      exportEditor, listNodes, drag, drop, allowDrop, dialogVisible, dialogData
    }

  }
}

</script>
